        <h3 class="text-center pb-4">
          Why the Violence in Nima is a Systemic Issue
        </h3>
        <div class="text-center">
          <img
            src="assets/images/conflict.jpg"
            class="nimasec-pic"
            width="1000"
            alt="Conflict"
          />
        </div>
        <p class="pt-4">
          In recent news, a clash between what appears to be two factions of
          local groups has ensued. As of 19th January, unconfirmed reports from
          “Myjoyonline” suggests that at least 4 persons have died as a result.
          While this is a horrendous event that mars the image of peace that
          Ghana has long been identified with, it may be a symptom of an
          under-arching exploitation of the social structure found in those
          communities. The purpose of this article is to investigate this social
          structure and to perhaps provide a rational anchoring to this truly
          tragic event in the history of Ghana.
        </p>
        <p>
          The first point I would like to highlight is the self-selecting nature
          of these communities. It is common knowledge that Nima which is in the
          East Ayawaso district in Accra is a slum settlement. Research from a
          peer-reviewed journal authored by Stephen Takyi and his team suggests,
          as obvious as it appears, that the calibre of people that reside in
          slums are a great subsection of the lower echelons of society (2477).
          Academic researchers Baomin Dong and colleagues also put forward that
          poverty is the major driving force of violent crime (1). Given the
          trend of these causal links, it is not unreasonable to suggest that
          part of the causes of the high crime rate in Nima (amongst other
          slums) is due to the pervasiveness of poverty in such regions. What is
          more important than these facts is that these communities of
          poverty-stricken people do not spontaneously erupt from the population
          but are by themselves chosen by this self-selecting process.
        </p>
        <p>
          Additionally, another fact of biological significance is that
          poverty-stricken groups are up to 3 times more likely to face
          depression as pointed out by Mathew Ridley and his team of
          professionals (1). Depression negates feelings of hope and optimism
          for the future. It then follows that if you leave a community that is
          much more likely to be faced with hopelessness and is self-selecting
          towards people who share the same sentiment, the foundation of this
          community will be precarious at best and catastrophic at worst. To
          quote James Baldwin, “The most dangerous creation of any society is
          the man who has nothing to lose”. This speaks to the point I am trying
          to put across, only in this case, you have the society not just of one
          man but of many men who may have “nothing” to lose. It is difficult
          for conflicts to not ensue from such a society.
        </p>
        <p>
          Another factor that seems to contribute to the increasingly violent
          nature of communities like Nima is the exploitation of in-group
          loyalty and out-group aggression. Ghana as a whole is a country that
          is fostered with a sense of oneness. This fact doesn’t only hold at a
          national level but it is even more profound on the local community
          level. While this quality is not egregious in its own right, it can
          just like any other force of good in the world, be wielded towards
          unproductive ends. Group identity is something that appears to be
          grounded in our biology. It is profound in the fact that it doesn’t
          only present itself in humans but in other organisms within the animal
          kingdom ranging from chimpanzees to bonobos and is even present in
          caste systems in bees and termites (although I hasten to say that
          “identity” may not be the right word). However, alongside in-group
          systems lay out-group interactions. Out-group interactions are
          essentially the way in which different in-group units relate to one
          another. More often than not these types of interactions revolve
          around discriminatory and aggressive behaviours which is not uncommon
          to the rest of the animal kingdom (chimpanzees are a notorious
          example). Communities like Nima seem to greatly utilise this
          predisposition of individuals to engage in groups and identify
          themselves within them. This is where I suppose the problem lies. As I
          have shown from analogous claims in the animal kingdom, it is fairly
          easy for humans to aggregate into groups and even easier for out-group
          aggression to ensue as a result. And so again when we have an
          unfortunate group of depressed, group-longing, low income dwellers in
          one region of space it is not surprising that stratification is
          observed which results in violent interactions.
        </p>
        <p>
          In the previous two paragraphs, I have spoken about what the probable
          causes of violent interactions between local communities may be. In
          the next two, I would like to give possible solutions to them. First
          and foremost, it is important that in discussing such issues of great
          concern that we are not tempted to make what I would describe as
          hubristic claims like, “They are so evil” or “These hooligans should
          all be killed” etc. The reason I assert that it would be arrogant to
          do so is because of the “accident of birth”. This is the notion of the
          arbitrariness of where we are born or what families we are fortunate
          to have. This concept should not make us pompous in the face of
          calamities of such a scale, but it should be a point of humble
          reflection charged by a determination to do as much as one can to
          alleviate situations of these sorts. The first step to begin to solve
          problems like these is to realize that the playing field is not equal,
          the lottery of life is not fair either but what lies within our domain
          of action will always be our empathic response to such plights.
          Trivial as this point seems it is crucial to the kinds of steps we
          take to solve social and moral problems in our country.
        </p>
        <p>
          Another solution would be to reconstruct slums into proper abodes that
          support human flourishing. But it should not end there. As it is
          widely known it is far harder to get rid of a trait than it is to
          learn one and even more so if a community exists to reinforce the said
          behaviour that is up for re-learning. This is why it seems to be more
          apt to incorporate what I call the “dilution strategy” as soon as
          proper housing structures are made in such localities. The dilution
          strategy involves spreading inhabitants within slums into wider
          distinct geographical areas. This, coupled with decent work will not
          only expedite the unlearning of such out-group aggression traits but
          will expose children and adults alike to the socialisation of healthy
          lifestyles in other well-to-do communities. Although at the end of the
          day the change must be intrinsically motivated, it is my firm belief
          that opportunities like these will at least spark flames in these
          individuals that will set off the blazing trail that surely lies ahead
          of them- a trail of peace coupled with flourishing.
        </p>
