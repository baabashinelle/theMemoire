        <h3 class="text-center pb-4">The Rastafarian Conundrum</h3>
        <div class="text-center">
          <img
            src="assets/images/rasta.jpg"
            width="1000"
            alt="Rastafarian"
            class="rasta-sec-pic"
          />
        </div>
        <p class="pt-4">
          Before I delve into this issue, I would like to establish a premise. I
          argue that questions of virtue should not be argued from the lens of
          reasoning but can invoke it to buttress a point (not to establish it).
          An illustration of this effect is a contrast between a Muslim, who
          does not eat pork because it is inherently wrong to do so and a person
          who decides not to eat pork because it is a healthier alternative.
          These people are not equivalent as one is solely on the basis of
          virtue and the other is not. Suppose we had a sauce that previously
          contained pork meat. The Muslim will be less likely to eat that, as
          opposed to the latter individual. It is a simple illustration of
          virtue and reasoning. We can go on to ask the Muslim why he/she does
          not eat pork. It is not far-fetched to imagine that the individual
          will begin to argue from a standpoint of reasoning by saying that it
          contains unhealthy fat and it houses a lot of parasites. That is a
          fair argument from a logical perspective but if we were to create a
          lump of hypothetical pork meat that was 100% rid of its fat and
          parasites, would the Muslim then be inclined to eat it? If not (the
          most likely answer), then we can establish that the claim to not eat
          pork is deeply rooted in virtue and not reasoning as it were.
        </p>
        <p>
          You may ask why is this important given that the title of this chapter
          is the “Rastafarian conundrum”. Well, it is essential in finding
          whether a claim is just or not which is what we are going to try to
          reason around with regards to this issue. In the Ghanaian system of
          education, to proceed to acquire secondary education, one must first
          participate in the annual Basic Education Certification Examination.
          Placement of successful students is then done by a Computerised School
          Selection and Placement System (CSSPS). On 19th March 2021, following
          the release of the placement system, two boys went to Achimota
          Secondary school- one of the most prestigious secondary schools in
          Ghana- to do a routine registration, since they were granted admission
          by the said computerised system. Surprisingly, they were denied entry
          on some interesting grounds. These brilliant boys, Tyron Iras Marhguy
          and Ohenba Kwaku Nkrabea (Lartey, 2021) were not admitted on the basis
          of their dreadlocks, the signature of their religious and cultural
          heritage. Surely as you read this you ask questions like, “Where is
          their right to education” and “where is their right to freedom of
          religion” conversely, you may assert, “Well, if they cannot comply
          with the rules of the said institution, they should just leave”. Many
          of the proponents of the “admit the Rastafarians” argue their
          frustrations on the foundations of the fundamental right every human
          being is entitled to. While the opponents of this cause claim, as I
          have above mentioned, that it is merely a situation of if you go to
          Rome and ca not do what the Romans do, leave. It is noteworthy that
          some institutions like the Ghana Education Service, GES (at one
          point), the Coalition of Ghana’s independence have supported the cause
          to allow the students entry into Achimota. Other institutions like the
          Old Achimotans Association, the National Association of Graduate
          Teachers (NAGRAT), led by their president, Angel Carbonu (Ansah, 2021)
          have adamantly resisted (in the person of their president) the cause
          and vowed even, to be voluntarily engaged in opposing any lawsuits
          demanding admission of the said students.
        </p>
        <p>
          Let us begin to dissect this issue. Mr Angel Carbonu argues that there
          would not be an enabling environment for teaching and learning if all
          students are allowed to freely and fully practice their beliefs.
          Achimota Secondary school, which is a Christian school, allows and
          even to an extent forces its members to engage in all mandatory
          Christian programs it holds. If Christians in Achimota are presumably
          allowed to, in this case fully and freely practise their beliefs, why
          not the Muslims or the Rastafarians? Secondly, by asserting that
          students should not be allowed to fully and freely practise their
          beliefs while conversely allowing Christians to do otherwise, we
          inadvertently put one religion on a pedestal, in a way that the law
          does not or in this case should not permit. On the other side of the
          coin, Mr Carbonu does raise an important issue that I suppose is
          hidden in the statement “freely” and “fully”. In the scenario in which
          the said boys were Muslims, would it be as wrong to disallow them from
          engaging in their daily prayers at times that clash with the school’s
          timetable as it is to prevent Rastafarians from keeping their
          dreadlocks? The answer, I reckon, should be no because an obvious and
          fundamental difference is that while in one case, there is a
          disruption in the very system, the other case has no bearing on any
          third party or system. If the argument is that the dreadlocks can
          stink when it is not well kept, then we go outside the scope of the
          argument itself. It ceases to be a question of the right to education
          and the right to freedom of religion and commences to be a question of
          good hygiene which all individuals are to uphold in order to ensure a
          conducive environment for learning.
        </p>
        <p>
          Lartey (2021) explains that the chairman of the Achimota PTA, Dr Andre
          Kwasi-Kumah argues that, “The situation cannot be compromised because
          allowing students to come into the school with their own choice of
          hair will breed indiscipline.” At the root of this argument is a case
          of what the Ghanaian populace, I believe, agrees as a virtue worth
          upholding and fighting for. This, I hypothesize, is more than just a
          fight for discipline but more of a fight for the continued
          entrenchment of these virtues that the majority of Ghanaians have
          grown to love and appreciate. Because I suspect that even given a
          system where indiscipline is unduly affected by the acceptance of
          Rastafarians, there would still be arguments raised about the issue.
          If this remains the matter, we must ask ourselves if we are
          utilitarians because if the case of whether something is right or
          wrong is predicated on who or what it can affect indirectly, rather
          than whether it is inherently wrong or otherwise, our moral
          foundations will prove to be rather precarious. It is worth saying
          that, while I make the assumption about inherent goods, that is things
          that are right just because they are (as Kant will want us to
          believe), it does not take away from the fact of the matter, that it
          is still worth considering the basis upon which we define an act as
          good. Sam Harris, a philosopher, also puts forward an interesting take
          on how the good can be defined. His answer is Science. In his book
          “The Moral Landscape”, he asserts that morality can be attained from
          the knowledge about “well-being” and “states of the brain” both of
          which can be determined to some extent by the rigour of Science. In
          that world view, the scrutiny about whether denying the Rastafarians
          admission is right will be calculated as a consequential product of
          the manner in which it affects well-being and states of the brain
          (I.e. psychological etc). This as well as other forms of morality
          world views like moral relativism would still require an introspection
          of the foundation upon which we will define “the good” just like I
          initially argued.
        </p>
        <p>
          If the argument from the other side is that the hairdo of a person
          does not characterise the content of one’s character, why are things
          like wearing a formal suit during an interview or having neatly combed
          hair seem non-negotiable? If none of these describe the content of a
          person’s character, then why do we uphold them? Likewise, if we
          justify this reasoning on solely the grounds of reasoning alone, then
          are we right in defending other customary practices like shaving one’s
          beard in school, wearing white to a naming ceremony or even greeting
          with the right hand? Let us use a powerful philosophical principle by
          John Rawls, the veil of ignorance, to see a possible answer. The veil
          of ignorance represents a state in which we do not, as individuals,
          know the outcome of our lives. We then use that uncertainty to make
          laws and policies that govern us all. Rawls argues that it is only
          under this veil that we may produce a way of life that is just for
          all. Supposing all together as stakeholders we converge behind the
          veil of ignorance to decide what policies should govern. Behind it, I
          reason that none of us will choose to prevent religion, looks,
          disabilities (that do not unduly affect the nature of work we choose
          to engage in) and the likes to be barricades to our access to
          resources in our nation. This is because the principle prevents us
          from knowing which end of the religious, appearance, and ability
          spectrum we would be lucky or unlucky to occupy.
        </p>
        <p>
          But how practical would this be given that we have registered, as a
          society, that things like greetings, looks and mannerisms give us a
          quick and sometimes accurate way of determining the content of an
          individual’s character? I would argue that things of this sort evolved
          with society for the sole purpose of making personal and timely (and
          sometimes accurate) decisions about people we meet briefly in the
          course of life. But this 6th sense, I reckon, should end at the
          doorstep of briefly meeting the individual. For example, if you are
          approaching a dark alley and you see 3 very odd-looking men coming
          from the opposite direction, chances are you will try using any other
          possible route. You will probably be wise to do so because common
          sense and the mere outcome of events past, give you a reason to trust
          the likely probability that you will be jumped by these strangers
          relative to some ordinary-looking men. But this should not be the
          dogma for judging people you have the potential of knowing for
          extended periods of time. In short, the caveat of premature judgements
          of persons should just be limited to instances where it would be
          otherwise impossible to ascertain the true moral character and motive
          of an individual, given that that decision is made when one’s back is
          against the wall, in my opinion.
        </p>
        <p>
          Jeremy Bentham, the founder of utilitarianism would have suggested, I
          suppose, that we take utility to find what the greatest pleasure is
          and that will be the just thing to do. In this case, Bentham would
          have possibly argued that if enough people want the Rastafarians to be
          admitted, that should be the case and vice versa. But we see a clear
          problem with that. Values should not be dynamic but inert (if we are
          not looking through the lens of moral relativism or moral nihilism).
          Interestingly, Sam Harris’ take on morality, which he illustrates as a
          landscape with multiple peaks and valleys will not make this statement
          any less true. The peaks in his moral landscape represent moral
          decisions that maximise human well-being and by extension, are the
          morally correct answers. The valleys represent the opposite analogy.
          In this world view, even if there are multiple morally correct ways of
          handling the Rastafarian conundrum, they will still be inert and by
          extension be a value. Hence, looking at it through Sam Harris’ moral
          landscape will not take away from this claim of the inertness of
          values. The only problem now is that we seem to be in a state of
          epistemic relativism- fighting about what possibly amounts to the
          right value to uphold since everybody’s system of values is valid from
          their reference.
        </p>
        <p>
          On the other hand, Kant the pioneer of the deontological argument
          would have championed a search for an absolute system of morality that
          is right given any context- something he calls the categorical
          imperative. As of 5th March 2021, the verdict from the Accra High
          Court has been given and the Rastafarians have won under the banner of
          human rights administration. But I believe a deeper problem has been
          put forward, brought to light by the Rastafarian saga. It is a
          question that requires that we have a civic debate about the good
          life, one like the political philosopher, Michael Sandel proposes. The
          goal of this would not be to make us all have an absolute
          understanding but to merely be a pivot to learning, dismissing the
          illogical and null points and revising the arguments of both sides to
          decide what is truly just.
        </p>
